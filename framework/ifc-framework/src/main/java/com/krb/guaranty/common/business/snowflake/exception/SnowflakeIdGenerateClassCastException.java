package com.krb.guaranty.common.business.snowflake.exception;

import com.krb.guaranty.common.framework.exception.GuarantyException;

/**
 * 自动生成id类型转换异常
 */
public class SnowflakeIdGenerateClassCastException extends GuarantyException {
    public SnowflakeIdGenerateClassCastException() {
        super();
    }

    public SnowflakeIdGenerateClassCastException(String message) {
        super(message);
    }

    public SnowflakeIdGenerateClassCastException(String message, Throwable cause) {
        super(message, cause);
    }

    public SnowflakeIdGenerateClassCastException(Throwable cause) {
        super(cause);
    }

    public SnowflakeIdGenerateClassCastException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
